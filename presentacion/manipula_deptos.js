function matPrimas(){
		  var mat = document.getElementById("matPrima");
		  
		  //var caja = document.getElementsByClassName("cont_caja");
		  
		  var m = document.getElementById("mascota");
		  var e = document.getElementById("electronica");
		  
		  if (mat.style.display === "none") {
			mat.style.display = "block";
			
			//caja.style.display = "none";
			
			m.style.display = "none";
			e.style.display = "none";
		  } else {
			mat.style.display = "none";
			
			m.style.display = "none";
			e.style.display = "none";
			
			//caja.style.display = "none";
		  }
		}
